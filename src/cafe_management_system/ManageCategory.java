    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

        String name, id = null, name1 = null;

        try {
            name = txtcategory.getText();
            String q1 = "insert into category(name) values('" + name + "')";
            String q2 = "select * from category";
            conn c = new conn();
            c.s.executeUpdate(q1);
            ResultSet rs = c.s.executeQuery(q2);
            while (rs.next()) {
                id = String.valueOf(rs.getInt("id"));
                name1 = rs.getString("name");
            }           
            JOptionPane.showMessageDialog(null, "Category Added Successfully!");
           String tbdata[] = {id, name1};
            DefaultTableModel tdf = (DefaultTableModel) table.getModel();
            tdf.addRow(tbdata);
            txtcategory.setText("");
        save.setEnabled(false);
        } catch (Exception e) {
        }
    }//GEN-LAST:event_saveActionPerformed

