    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        DefaultTableModel tdf1 = (DefaultTableModel) table.getModel();
        String nam=tdf1.getValueAt(table.getSelectedRow(),1).toString();
        String str="delete from category where name='"+nam+"'";
        conn c=new conn();
        try{
            c.s.executeUpdate(str);
        }catch(Exception e){}
        int s = JOptionPane.showConfirmDialog(null, "Are you Really Want to delete this Data?", "Select", JOptionPane.YES_NO_OPTION);
        if (s == 0) {
            tdf1.removeRow(table.getSelectedRow());
        }
    }//GEN-LAST:event_tableMouseClicked

